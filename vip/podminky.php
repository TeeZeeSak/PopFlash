                    <h4>VII. Odstoupení, výpověď a reklamace</h4>
                    <ol class="text-left">
                        <li>Kupující-spotřebitel má právo na odstoupení od kupní smlouvy pouze v případě, pokud nebylo
                          zakoupené zboží/služba nijak použito. Použité zboží/služba nastává v případě, že byl odeslán
                          na email potvrzovací email po úspěšném zadání aktivačního kódu. Odstoupení je v takovém případě
                          možné nejpozději do třiceti dní od zakoupení zboží/služby. Prodávající následně vrátí
                          kupujícímu-spotřebiteli kupní cenu bez zbytečného odkladu, a to bezhotovostně na účet určený
                          kupujícím-spotřebitelem.
                        </li>
                        <li>Kupující-spotřebitel nemá právo odstoupit od kupní smlouvy podle § 1837/l OZ, protože zboží/služba
                            má digitální obsah, není dodáváno na hmotném nosiči a je dodáváno s předchozím výslovným
                            souhlasem kupujícího-spotřebitele před uplynutím lhůty pro odstoupení od kupní smlouvy a
                            prodávající před uzavřením kupní smlouvy sdělil kupujícímu-spotřebiteli, že nemá právo na
                            odstoupení od smlouvy v případě, že zboží použil a tím ho nenávratně znehodnotil.
                        </li>
                        <li>Do doby převzetí zboží kupujícím-spotřebitelem je prodávající oprávněn kdykoliv od kupní
                            smlouvy odstoupit. V takovém případě vrátí prodávající kupujícímu-spotřebiteli kupní cenu
                            bez zbytečného odkladu, a to bezhotovostně na účet určený kupujícím-spotřebitelem nejpozději
                            do 14 dnů.
                        </li>
                        <li>Prodávající a kupující mají právo kdykoliv a bez udání důvodu vypovědět smlouvu o
                            registraci.
                            Smlouva o registraci pozbývá účinnosti uplynutím třídenní výpovědní doby, která začíná
                            plynout
                            ode dne následujícího po doručení druhé smluvní straně.
                        </li>
                        <li>V případě, že chce kupující-spotřebitel zboží/službu z nějakého důvodu reklamovat, musí se obrátit na email
                        provozovatele a to PopFrag@PopFrag.eu, kde proběhne odborné přezkoumání vzniklého problému. Provozovatel je povinen
